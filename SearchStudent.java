    private void loadAllStudents() {// Shows all the student whenever the search appears
        String sqlQuery = "SELECT sd.student_ID, sd.student_name, sr.program AS course, sr.yr_lvl "
                          + "FROM student_details sd "
                          + "INNER JOIN student_records sr ON sd.student_ID = sr.student_ID";

        try (Connection con = Connect_DB.connect();
             PreparedStatement pst = con.prepareStatement(sqlQuery);
             ResultSet rs = pst.executeQuery()) {

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0); // Clear existing rows

            while (rs.next()) {
                model.addRow(new Object[] {
                    rs.getString("student_ID"),
                    rs.getString("student_name"),
                    rs.getString("course"),
                    rs.getInt("yr_lvl")
                });
            }

        } catch (SQLException e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Error loading student data: " + e.getMessage());
        }
    }
    
