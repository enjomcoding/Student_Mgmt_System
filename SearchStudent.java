    private void delBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delBttnActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        int[] selectedRows = jTable2.getSelectedRows(); // Get selected rows

        if (selectedRows.length == 0) {
            showMessage("Please select at least one row to delete.");
            return;
        }

        if (!confirmDeletion()) {
            return;
        }

        try (Connection con = Connect_DB.connect()) {
            con.setAutoCommit(false); // Start transaction

            // Prepare delete statements
            try {
                for (int row : selectedRows) {
                    String studentID = (String) model.getValueAt(row, 0); // Get the Student ID from the table
                    deleteStudentRecords(con, studentID);
                }

                con.commit(); // Commit the transaction
                removeRowsFromTable(model, selectedRows);
                showMessage("Selected row(s) deleted successfully.");
            } catch (SQLException e) {
                con.rollback(); // Rollback transaction in case of error
                throw e;
            }
        } catch (SQLException e) {
            e.printStackTrace();
            showMessage("Error deleting row(s): " + e.getMessage());
        }
    }//GEN-LAST:event_delBttnActionPerformed

    private String getEnrollmentStatus() {
        if (trnsRb.isSelected()) {
            return "Transferee";
        } else if (rtrRb.isSelected()) {
            return "Returnee";
        } else if (newRb.isSelected()) {
            return "New student";
        } else if (oldRb.isSelected()) {
            return "Old student";
        }
        return "";
    }
    
    private String buildSqlQuery(String enrolStat, String keyword) {
        StringBuilder sqlQuery = new StringBuilder("SELECT sd.student_ID, sd.student_name, sr.program AS course, sr.yr_lvl "
                                                  + "FROM student_details sd "
                                                  + "INNER JOIN student_records sr ON sd.student_ID = sr.student_ID ");

        boolean hasEnrollmentStatus = !enrolStat.isEmpty();
        boolean hasKeyword = !keyword.isEmpty();

        if (hasEnrollmentStatus) {
            sqlQuery.append("WHERE sr.enrol_stat = ?");
        }

        if (hasKeyword) {
            if (hasEnrollmentStatus) {
                sqlQuery.append(" AND ");
            } else {
                sqlQuery.append("WHERE ");
            }
            sqlQuery.append("(sd.student_name LIKE ? OR sd.student_ID LIKE ? OR sr.program LIKE ? OR sr.yr_lvl LIKE ?)");
        }

        return sqlQuery.toString();
    }
    
    private void setQueryParameters(PreparedStatement pst, String enrolStat, String keyword) throws SQLException {
        int parameterIndex = 1;

        if (!enrolStat.isEmpty()) {
            pst.setString(parameterIndex++, enrolStat);
        }

        if (!keyword.isEmpty()) {
            String likeKeyword = "%" + keyword + "%";
            pst.setString(parameterIndex++, likeKeyword);  // student_ID
            pst.setString(parameterIndex++, likeKeyword);  // student_name
            pst.setString(parameterIndex++, likeKeyword);  // program
            pst.setString(parameterIndex++, likeKeyword);  // yr_lvl
        }
    }
    
    private void populateTable(ResultSet rs) throws SQLException {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0); // Clear existing rows

        while (rs.next()) {
            model.addRow(new Object[] {
                rs.getString("student_ID"),   // student_id
                rs.getString("student_name"), // student_name
                rs.getString("course"),       // program (alias as course)
                rs.getInt("yr_lvl")           // year
            });
        }
    }
    
    private boolean confirmDeletion() {
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to delete the selected row(s)? This action cannot be undone.", 
            "Confirm Deletion", 
            javax.swing.JOptionPane.YES_NO_OPTION);
        return confirm == javax.swing.JOptionPane.YES_OPTION;
    }
    
    private void deleteStudentRecords(Connection con, String studentID) throws SQLException {
        String[] deleteQueries = {
            "DELETE FROM student_details WHERE student_ID = ?",
            "DELETE FROM student_demo WHERE student_ID = ?",
            "DELETE FROM student_fam WHERE student_ID = ?",
            "DELETE FROM student_records WHERE student_ID = ?"
        };

        for (String query : deleteQueries) {
            try (PreparedStatement pst = con.prepareStatement(query)) {
                pst.setString(1, studentID);
                pst.executeUpdate();
            }
        }
    }
    
    private void removeRowsFromTable(DefaultTableModel model, int[] selectedRows) {
        // Remove rows from the table model (reverse order to avoid index issues)
        for (int i = selectedRows.length - 1; i >= 0; i--) {
            model.removeRow(selectedRows[i]);
        }
    }
    
