    private void loadAllData() {// Shows all the prof whenever the search appears
        String sqlQuery = "SELECT profID, prof_name, position, dept, spec"
                        + " FROM professor_details";


        try (Connection con = Connect_DB.connect();
            PreparedStatement pst = con.prepareStatement(sqlQuery);
            ResultSet rs = pst.executeQuery()) {

            DefaultTableModel model = (DefaultTableModel) searchTable.getModel();
            model.setRowCount(0); // Clear existing rows

            while (rs.next()) {
                model.addRow(new Object[] {
                    rs.getString("profID"),
                    rs.getString("prof_name"),
                    rs.getString("position"),
                    rs.getString("dept"),
                    rs.getString("spec")
                });
            }

        } catch (SQLException e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Error loading professor data: " + e.getMessage());
        }
    }
    
