    private String buildSqlQuery(String keyword) {
        StringBuilder sqlQuery = new StringBuilder("SELECT profID, prof_name, position, dept, spec "
                                    + "FROM professor_details ");

        boolean hasKeyword = !keyword.isEmpty();

        if (hasKeyword) {
            sqlQuery.append("WHERE "); // Start the WHERE clause
            sqlQuery.append("(prof_name LIKE ? OR profID LIKE ? OR position LIKE ? OR dept LIKE ? OR spec LIKE ?)"); // Use correct columns
        }

        return sqlQuery.toString();
    }
    
    private void setQueryParameters(PreparedStatement pst, String keyword) throws SQLException {
        int parameterIndex = 1;

        if (!keyword.isEmpty()) {
            String likeKeyword = "%" + keyword + "%";
            pst.setString(parameterIndex++, likeKeyword);  // prof_name
            pst.setString(parameterIndex++, likeKeyword);  // profID
            pst.setString(parameterIndex++, likeKeyword);  // position
            pst.setString(parameterIndex++, likeKeyword);  // dept
            pst.setString(parameterIndex++, likeKeyword);  // spec
        }
    }
    
    private void populateTable(ResultSet rs) throws SQLException {
        DefaultTableModel model = (DefaultTableModel) searchTable.getModel();
        model.setRowCount(0); // Clear existing rows

        while (rs.next()) {
            model.addRow(new Object[] {
                rs.getString("profID"),
                rs.getString("prof_name"),
                rs.getString("position"),
                rs.getString("dept"),
                rs.getString("spec")
            });
        }
    }
    
    private boolean confirmDeletion() {
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to delete the selected row(s)? This action cannot be undone.", 
            "Confirm Deletion", 
            javax.swing.JOptionPane.YES_NO_OPTION);
        return confirm == javax.swing.JOptionPane.YES_OPTION;
    }
    
    private void deleteProf(Connection con, String profID) throws SQLException {
        String query =  "DELETE FROM professor_details WHERE profID = ?";

        try (PreparedStatement pst = con.prepareStatement(query)) {
            pst.setString(1, profID);
            pst.executeUpdate();
        }
    }
    
    private void removeRowsFromTable(DefaultTableModel model, int[] selectedRows) {
        // Remove rows from the table model (reverse order to avoid index issues)
        for (int i = selectedRows.length - 1; i >= 0; i--) {
            model.removeRow(selectedRows[i]);
        }
    }
    
